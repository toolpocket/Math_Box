# 기본 계산기
def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def get_Median(a, b):
    return (a + b) / 2


def get_Remainder(a, b):
    return a // b


def get_abs(num):
    if num >= 0:
        return num
    else:
        return -num


def get_Percentage(a, b):
    return (a / b) * 100
